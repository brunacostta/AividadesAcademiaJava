package Java2_poo_atp22;

public class Calculadora {
    public int soma(int s1, int s2) {
        return s1 + s2;
    }

    public double soma(double d1, double d2){
        return d1 + d2;
    }

    public float soma(float f1, float f2, float f3) {
        return f1 + f2 + f3;
    }

    public long soma(long l1, long l2, long l3) {
        return l1 + l2 + l3;
    }
}
