package Java2_poo_atp30;

public class Calculadora {
    public int n1;
    public int n2;

    public int soma(int n1, int n2) {
         return n1 + n2;
    }

    public int subtracao(int n1, int n2) {
        return n1 - n2;
    }

    public double divisao(Double n1, Double n2) {
        return n1 / n2;
    }

    public int multiplicacao(int n1, int n2) {
        return n1 * n2;
    }
        
}

